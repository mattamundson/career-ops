#!/usr/bin/env node
/**
 * prune-pipeline-tracked.mjs — Remove pending pipeline lines whose job identity
 * already appears in a tracker report (`**URL:**` in reports/*.md linked from data/applications.md).
 *
 * Matching uses keys from `scripts/lib/job-url-keys.mjs` (jid, li, indeed, lever, builtin, url).
 *
 * Default: dry-run. Use --apply to write files (creates .bak) and append data/pipeline-prune-log.md.
 *
 *   node scripts/prune-pipeline-tracked.mjs
 *   node scripts/prune-pipeline-tracked.mjs --apply
 *   node scripts/prune-pipeline-tracked.mjs --apply --only data/pipeline.md
 */

import { readFileSync, writeFileSync, existsSync, copyFileSync, appendFileSync } from 'fs';
import { resolve, dirname, join } from 'path';
import { fileURLToPath } from 'url';

import { jobKeysFromUrl } from './lib/job-url-keys.mjs';
import { loadTrackedKeyMap } from './lib/tracker-report-keys.mjs';

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const APPLY = process.argv.includes('--apply');
const ONLY = (() => {
  const i = process.argv.indexOf('--only');
  if (i === -1 || !process.argv[i + 1]) return null;
  return resolve(ROOT, process.argv[i + 1].replace(/^\.\//, ''));
})();

const APPS = join(ROOT, 'data', 'applications.md');
const LOG = join(ROOT, 'data', 'pipeline-prune-log.md');
const PIPE = join(ROOT, 'data', 'pipeline.md');
const RAW = join(ROOT, 'data', 'pipeline-raw-intake.md');

function isPendingLine(line) {
  return /^\s*-\s*\[\s*\]\s+https?:\/\//i.test(line);
}

function firstUrlInLine(line) {
  const m = line.match(/https?:\/\/[^\s|)]+/i);
  return m ? m[0].trim() : null;
}

function processPipelineFile(absPath, keyTo, label) {
  if (!existsSync(absPath)) {
    console.log(`Skip (missing): ${label}`);
    return { path: absPath, removed: [], skipped: true };
  }
  const raw = readFileSync(absPath, 'utf8');
  const lines = raw.split(/\r?\n/);
  const out = [];
  const removed = [];

  for (const line of lines) {
    if (!isPendingLine(line)) {
      out.push(line);
      continue;
    }
    const url = firstUrlInLine(line);
    if (!url) {
      out.push(line);
      continue;
    }
    const lineKeys = jobKeysFromUrl(url);
    let hit = null;
    for (const k of lineKeys) {
      if (keyTo.has(k)) {
        hit = { key: k, meta: keyTo.get(k) };
        break;
      }
    }
    if (hit) {
      removed.push({ line, matchKey: hit.key, apps: hit.meta.appNums.slice(0, 5), company: hit.meta.firstCompany });
    } else {
      out.push(line);
    }
  }

  return { path: absPath, out: out.join('\n'), removed, skipped: false };
}

function main() {
  const { keyTo, missingReport, noUrlInReport, entryCount } = loadTrackedKeyMap(ROOT, APPS);
  console.log(`Tracker rows parsed: ${entryCount}`);
  console.log(`Distinct match keys from reports: ${keyTo.size}`);
  if (missingReport) console.log(`Rows skipped (missing report file): ${missingReport}`);
  if (noUrlInReport) console.log(`Rows skipped (no **URL:** in report): ${noUrlInReport}`);

  const targets = ONLY ? [ONLY] : [PIPE, RAW];
  let totalRemoved = 0;

  for (const absPath of targets) {
    const label = absPath.replace(ROOT + '/', '');
    const { out, removed, skipped } = processPipelineFile(absPath, keyTo, label);
    if (skipped) continue;

    const removeVerb = APPLY ? 'removed' : 'would remove';
    console.log(`\n${label}: ${removeVerb} ${removed.length} pending line(s)`);
    const preview = removed.slice(0, 15);
    for (const r of preview) {
      const short = r.line.slice(0, 100).replace(/\s+/g, ' ');
      console.log(`  match ${r.matchKey} apps=#${r.apps.join(',')} :: ${short}${r.line.length > 100 ? '…' : ''}`);
    }
    if (removed.length > preview.length) console.log(`  … and ${removed.length - preview.length} more`);

    totalRemoved += removed.length;

    if (APPLY && removed.length) {
      copyFileSync(absPath, `${absPath}.bak`);
      writeFileSync(absPath, out, 'utf8');
      console.log(`Wrote ${label} (+ ${label}.bak)`);

      const stamp = new Date().toISOString();
      const logBlock = [
        '',
        `### ${stamp} — ${label}`,
        '',
        `Removed **${removed.length}** line(s) already covered by tracker report URL keys.`,
        '',
        '| Match key | App # | Line |',
        '|-----------|--------|------|',
        ...removed.map((r) => {
          const esc = r.line.replace(/\|/g, '\\|').slice(0, 160);
          return `| \`${r.matchKey}\` | ${r.apps.join(', ')} | ${esc} |`;
        }),
        '',
      ].join('\n');
      if (!existsSync(LOG)) {
        writeFileSync(
          LOG,
          ['# Pipeline prune log (tracker-matched)', '', 'Append-only. Generated by `scripts/prune-pipeline-tracked.mjs`.', ''].join('\n'),
          'utf8',
        );
      }
      appendFileSync(LOG, logBlock, 'utf8');
      console.log(`Appended prune details to ${LOG.replace(ROOT + '/', '')}`);
    }
  }

  if (!APPLY && totalRemoved) {
    console.log(`\nDry-run only. Re-run with --apply to remove ${totalRemoved} line(s) total.`);
  }
  if (APPLY && !totalRemoved) {
    console.log('\nNothing to remove.');
  }
}

const isMain =
  process.argv[1] &&
  fileURLToPath(import.meta.url) === resolve(process.argv[1]);
if (isMain) main();
